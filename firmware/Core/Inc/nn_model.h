/**
 * @brief  YSA ile agirlik tahmini (Edge AI)
 * @param  ham_adc: Fuzyon sonrasi ORT ADC degeri (0-1207 arasi)
 * @retval Tahmin edilen kutle (kg, 0.0-4.0 arasi)
 */
float AI_Agirlik_Tahmin(uint32_t ham_adc) {
    /* Giris normalizasyonu: [0, MAX_ADC] -> [0, 1] */
    float input = ((float)ham_adc - NN_MIN_ADC) / (NN_MAX_ADC - NN_MIN_ADC);
    if (input < 0.0f) input = 0.0f;
    if (input > 1.0f) input = 1.0f;

    /* Katman 1: Dense(1 -> 32) + LeakyReLU(alpha=0.1) */
    float L1[32];
    for (int i = 0; i < 32; i++) {
        float val = input * NN_W1[i] + NN_B1[i];
        L1[i] = (val > 0.0f) ? val : val * 0.1f;
    }

    /* Katman 2: Dense(32 -> 16) + LeakyReLU(alpha=0.1) */
    float L2[16];
    for (int i = 0; i < 16; i++) {
        float sum = 0.0f;
        for (int j = 0; j < 32; j++) {
            sum += L1[j] * NN_W2[j * 16 + i];
        }
        float val = sum + NN_B2[i];
        L2[i] = (val > 0.0f) ? val : val * 0.1f;
    }

    /* Cikis Katmani: Dense(16 -> 1) */
    float kg = 0.0f;
    for (int i = 0; i < 16; i++) {
        kg += L2[i] * NN_W3[i];
    }
    kg += NN_B3[0];

    /* Guvenlik sinirlari */
    if (kg < 0.0f) kg = 0.0f;
    if (kg > 4.0f) kg = 4.0f;
    return kg;
}

